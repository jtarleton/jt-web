<h1>Scooby</h1>

<p>
<img src="images/Scooby-Doo-006.jpg"></img></p>

<p>Scooby is a blog application written in PHP using a MongoDB back end.  The data model resembles that of Wordpress, using a single posts collection and the shared taxonomy concept for both categories and tags.</p>

<p>The <a href="https://github.com/jtarleton/scooby">project repository</a> is hosted on github and is also available on <a href="https://www.packagist.org">packagist</a>.</p>


